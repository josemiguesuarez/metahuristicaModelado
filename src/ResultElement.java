
public class ResultElement {

	private int x;
	private int y;
	private int t;
	private int f;
	
	public ResultElement(int xT, int yT, int tT, int fT)
	{
		x=xT;
		y=yT;
		t=tT;
		f=fT;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getT() {
		return t;
	}

	public void setT(int t) {
		this.t = t;
	}

	public int getF() {
		return f;
	}

	public void setF(int f) {
		this.f = f;
	}
	
	
}
